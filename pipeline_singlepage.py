BOX_KWARGS = dict(boxstyle="round,pad=0.22", linewidth=1.3)

def box(ax, x, y, w, h, fc, ec, title, lines, title_color="black"):
    rect = mpatches.FancyBboxPatch((x - w/2, y - h/2), w, h,
                                    facecolor=fc, edgecolor=ec, **BOX_KWARGS)
    ax.add_patch(rect)
    top_edge = y + h/2
    title_y = top_edge - 0.30
    ax.text(x, title_y, title,
            fontsize=13, fontweight="bold", ha="center", va="center", color=title_color)
    line_y = title_y - 0.34
    for i, line in enumerate(lines):
        ax.text(x, line_y - i*0.30, line,
                fontsize=10.5, ha="center", va="center", color="#444444")

def arrow(ax, x1, y1, x2, y2, label="", lx=0, ly=0.12):
    ax.annotate("", xy=(x2, y2), xytext=(x1, y1),
                arrowprops=dict(arrowstyle="-|>", color="#555555", lw=1.3))
    if label:
        ax.text((x1+x2)/2 + lx, (y1+y2)/2 + ly, label,
                fontsize=9.5, color="#555555", ha="center")

