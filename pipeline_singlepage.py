# Classe positiva = unanswerable (quella che vogliamo rilevare)
POS = "unanswerable"
NEG = "answerable"


def evaluate(predictions, model_name):
    """
    Calcola le metriche di classificazione per un modello.
    Restituisce un dizionario con accuracy, precision, recall, F1
    globali e F1 per tipo di corruzione.
    """
    y_true = [p["label"]      for p in predictions]
    y_pred = [p["prediction"] for p in predictions]

    # ── Metriche per tipo di corruzione ──
    # Per le corrotte (entity/element/layout): F1 sulla classe unanswerable
    # Per le clean: accuracy (non ha senso F1 su una classe assente)
    per_type = {}
    corruption_types = sorted({p["corruption_type"] for p in predictions})

    for ctype in corruption_types:
        subset  = [p for p in predictions if p["corruption_type"] == ctype]
        yt      = [p["label"]      for p in subset]
        yp      = [p["prediction"] for p in subset]

        if ctype == "clean":
            per_type[ctype] = accuracy_score(yt, yp)
        else:
            per_type[ctype] = f1_score(
                yt, yp,
                pos_label=POS,
                labels=[NEG, POS],
                zero_division=0
            )

    return {
        "model"      : model_name,
        "acc"        : accuracy_score(y_true, y_pred),
        "prec"       : precision_score(y_true, y_pred, pos_label=POS, zero_division=0),
        "rec"        : recall_score(y_true, y_pred,    pos_label=POS, zero_division=0),
        "f1"         : f1_score(y_true, y_pred,        pos_label=POS, zero_division=0),
        "f1_macro"   : f1_score(y_true, y_pred, average="macro",      zero_division=0),
        "cm"         : confusion_matrix(y_true, y_pred, labels=[NEG, POS]),
        "parse_fail" : sum(p.get("parse_fail", False) for p in predictions),
        "per_type"   : per_type,
    }


