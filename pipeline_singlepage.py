def build_prompt(question, strategy="baseline"):
    if strategy == "baseline":
        return f"{BASE_INSTRUCTION}\n\nQuestion: {question}"

    if strategy == "few_shot":
        return f"{BASE_INSTRUCTION}\n\n{FEW_SHOT_EXAMPLES}\n\nQuestion: {question}"

    if strategy == "cot":
        return (
            f"Question: {question}\n\n"
            f"{COT_INSTRUCTION}"
        )

    if strategy == "knowledge":
        return f"{BASE_INSTRUCTION}\n\n{KNOWLEDGE_NOTE}\n\nQuestion: {question}"

    raise ValueError(f"Strategia sconosciuta: '{strategy}'")

def normalize_pred(raw):
    """
    Estrae la predizione finale dall'output grezzo del modello.
    Restituisce (predizione, parse_fail) dove parse_fail=True indica
    che il modello non ha prodotto una risposta conforme.

    Logica: vince l'ULTIMA occorrenza tra 'answerable' e 'unanswerable',
    perché i modelli CoT scrivono il ragionamento prima e la risposta alla fine.

    Caso tricky: 'unanswerable' contiene 'answerable' al suo interno
    (posizione iu+2), quindi va gestito esplicitamente.
    """
    text = (raw or "").lower()

    # Trova l'ultima occorrenza di ciascuna parola chiave
    pos_unanswerable = text.rfind("unanswerable")
    pos_answerable   = text.rfind("answerable")

    # Caso 1: nessuna parola chiave trovata -> output non conforme (es. modello estrattivo)
    if pos_unanswerable == -1 and pos_answerable == -1:
        return "answerable", True   # parse_fail=True: il modello non ha seguito le istruzioni

    # Caso 2: l'unico 'answerable' trovato è dentro 'unanswerable' (offset +2)
    if pos_unanswerable != -1 and pos_answerable == pos_unanswerable + 2:
        return "unanswerable", False

    # Caso 3: vince chi appare più tardi nel testo
    if pos_unanswerable > pos_answerable:
        return "unanswerable", False
    else:
        return "answerable", False


def run_inference(infer_fn, model_name, strategy="baseline"):
    """
    Orchestratore universale di inferenza.
    Gira su tutti i 200 campioni del benchmark e salva i risultati su Drive.

    Args:
        infer_fn   : funzione specifica del modello (infer_gemma, infer_qwen, ...)
        model_name : stringa identificativa per il nome del file JSON
        strategy   : "baseline" | "few_shot" | "cot" | "knowledge"
    """

    # CoT genera testo più lungo (ragionamento step-by-step)-> servono più token
    max_new_tokens = 64 if strategy == "cot" else 10

    results    = []
    fail_count = 0

    for i, sample in enumerate(benchmark_final):

        # Costruisce il prompt testuale per questo campione
        prompt = build_prompt(sample["question"], strategy)

        # Chiama il modello, in caso di errore registra stringa vuota e continua
        try:
            raw_output = infer_fn(
                resolve_image_path(sample["image"]),
                prompt,
                max_new_tokens
            )
        except Exception as e:
            print(f"[{model_name}] ERRORE campione {i}: {e}", flush=True)
            raw_output = ""

        # Normalizza l'output grezzo in ANSWERABLE / UNANSWERABLE
        prediction, parse_fail = normalize_pred(raw_output)
        fail_count += parse_fail

        # Salva il risultato per questo campione
        results.append({
            "id"             : sample["questionId"],
            "label"          : sample["label"],
            "prediction"     : prediction,
            "corruption_type": sample.get("corruption_type", "clean"),
            "question"       : sample["question"],
            "raw"            : raw_output.strip(),
            "parse_fail"     : parse_fail
        })

        # Stampa il progresso ogni 10 campioni
        if (i + 1) % 10 == 0:
            print(f"[{model_name} | {strategy}] {i+1}/{len(benchmark_final)}", flush=True)

    # Salva il JSON su Drive
    filename = f"predictions_{model_name}"
    if strategy != "baseline":
        filename += f"_{strategy}"

    output_path = os.path.join(PRED_DIR, f"{filename}.json")
    with open(output_path, "w") as f:
        json.dump(results, f, indent=2)

    print(f"\n[{model_name} | {strategy}] completato")
    print(f"  parse_fail : {fail_count}/{len(results)}")
    print(f"  salvato in : {output_path}")

    return results

